class _SimulationPreset {
  const _SimulationPreset({required this.id, required this.gameLength});

  final String id;
  final GameLengthConfig gameLength;

  BalanceTelemetryTuningTargets get targets {
    return BalanceTelemetryTuningTargets.forPaceProfile(gameLength.paceProfile);
  }
}

class _ScoreChaserFixture {
  const _ScoreChaserFixture({
    required this.id,
    required this.label,
    required this.state,
  });

  final String id;
  final String label;
  final PersistentGameState state;

  _ScoreChaserRow evaluate() {
    const activePlayerId = 'player_1';
    const leaderPlayerId = 'player_2';
    final activeScore = const EmpireScoreCalculator().scoreFor(
      playerId: activePlayerId,
      state: state,
    );
    final leaderScore = const EmpireScoreCalculator().scoreFor(
      playerId: leaderPlayerId,
      state: state,
    );
    final samples =
        BalanceTelemetryObjectiveActionDiagnostics.scorePressureSamplesFor(
          state: state,
          playerIds: const [activePlayerId, leaderPlayerId],
        );
    final sample = samples[activePlayerId];

    return _ScoreChaserRow(
      id: id,
      label: label,
      activeScore: activeScore.total,
      leaderScore: leaderScore.total,
      advice: sample?.advice.name ?? 'none',
      target: sample?.target.name ?? 'none',
    );
  }
}

class _ScoreChaserRow {
  const _ScoreChaserRow({
    required this.id,
    required this.label,
    required this.activeScore,
    required this.leaderScore,
    required this.advice,
    required this.target,
  });

  final String id;
  final String label;
  final int activeScore;
  final int leaderScore;
  final String advice;
  final String target;

  int get scoreGap => leaderScore - activeScore;
}
